<script>
import Loading from "@/components/loading";
import NoData from "@/components/NoData";
export default {
  components: { Loading, NoData },
  data() {
    return {
      isLoading: true,
      lectures: [],
      lecturePage: 1,
      lectureTotalPages: 1,

      exams: [],
      examPage: 1,
      examTotalPages: 1,
    };
  },
  created() {
    this.getSubjects();
    this.getExams();
  },
  methods: {
    openExam(exam) {
      this.$router.push(`/exam/${exam.id}?type_id=${this.$route.params.id}`);
    },
    getSubjects() {
      this.$axios
        .get(`/subject/${this.$route.params.id}/lecture`)
        .then((res) => {
          this.lectures = res.data.docs;
          this.lecturePage = res.data.page;
          this.lectureTotalPages = res.data.totalPages;
        })
        .finally(() => (this.isLoading = false));
    },

    getExams() {
      this.$axios
        .get(`/subject/${this.$route.params.id}/exams`)
        .then((res) => {
          this.exams = res.data;
        })
        .finally(() => (this.isLoading = false));
    },
  },
};
</script>

