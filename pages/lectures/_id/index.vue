      this.$axios
        .post(`/material/${this.currVideo.id}/comments`, {
          content: this.postAComment
        })
        .then(res => {
          this.postAComment = "";
          this.fetchCommentsOfMaterial();
        })
        .finally(() => loading.close());
    },
    postReply(content, comment) {
      if (content.trim() == "") {
        this.$vs.notification({
          color: "danger",
          position: "top-center",

          text: "اكتب رد أولاُ"
        });
        return;
      }
      const loading = this.$vs.loading();
      this.$axios
        .post(`/comments/${comment.id}/replies`, { content })
        .then(res => {
          comment.postAReply = "";
          if(comment.page < comment.totalPages){
              this.fetchRepliesOnComment(comment);
          }else{
              this.fetchRepliesOnComment(comment, true);
          }

        })
        .finally(() => loading.close());
    },
    fetchRepliesOnComment(comment,  paginated = false) {
      const loading = this.$vs.loading();
      this.$axios
        .get(`/posts/${comment.id}/replies?page=${comment.page || 1 }`)
        .then(res => {
          comment.replies = res.data.docs;
          comment.page = res.data.page;
          comment.totalPages = res.data.totalPages;
          // window.scrollTo({top:0, behavior: 'smooth'});




        })
        .finally(() => loading.close());
    },
    toggleReply(commentIndex) {
      // this.materialComments[commentIndex]
      if (this.currRepliy == commentIndex) {
        this.currRepliy = null;
        return;
      }
      this.currRepliy = commentIndex;
      this.fetchRepliesOnComment(this.materialComments[commentIndex]);
      if(this.materialComments[commentIndex].page < this.materialComments[commentIndex].totalPages){
            this.fetchRepliesOnComment(this.materialComments[commentIndex]);
        }else{
            this.fetchRepliesOnComment(this.materialComments[commentIndex], true);
        }
    },
    fetchCommentsOfMaterial() {
      const loading = this.$vs.loading();
      this.$axios
        .get(`/material/${this.currVideo.id}/comments?page=${this.page}`)
        .then(res => {
          res.data.docs.map(ele => (ele.replies = []));
          res.data.docs.map(ele => (ele.postAReply = ""));
          res.data.docs.map(ele => (ele.page = ""));
          res.data.docs.map(ele => (ele.totalPages = ""));


          this.currRepliy = null;

          this.materialComments = res.data.docs;
          this.page = res.data.page;
          this.totalPages = res.data.totalPages;
          // window.scrollTo({top:0, behavior: 'smooth'});
        })
        .finally(() => loading.close());
    },



    openExam(exam) {
      this.$router.push(`/exam/${exam.id}?type_id=${this.$route.params.id}`);
    },
    getExams() {
      this.$axios
        .get(`/lecture/${this.$route.params.id}/exams`)
        .then((res) => {
          this.exams = res.data;
        })
        .finally(() => (this.isLoading = false));
    },

    openPdf(file) {
      window.open(file, "_blank");
    },

    setCurrentVideo(video) {
      const loading = this.$vs.loading();
      this.currVideo = {};
      this.currVideo = { ...video };
      if(video.link && video.link.includes('youtube.com')){
        this.currVideoId = this.getVideoId(this.currVideo.link);
      }
      this.fetchCommentsOfMaterial();
      this.otherVideos = this.videos.filter(
          (video) => video.type == "video" && video.id != this.currVideo.id
      );
      this.files = this.videos.filter((video) => video.type == "pdf");
      loading.close();
    },
    seekToSecond(content) {
      console.log(this.$moment.duration(content.timing).asSeconds());
      // this.$moment.duration(content.timing).asSeconds();
      this.$refs.youtube.player.seekTo(
        this.$moment.duration(content.timing).asSeconds()
      );
    },
    getLectureVideos() {
      this.$axios
        .get(`/lecture/${this.$route.params.id}/material?paginate=false`)
        .then((res) => {
          this.videos = res.data;
          this.handleYoutubeAndVimeo();

          if (this.videos.length > 0) {
            this.currVideo = this.videos.find((ele) => ele.type == "video");
            this.fetchCommentsOfMaterial();
            this.currVideoId = this.getVideoId(this.currVideo.link);
            this.otherVideos = this.videos.filter(
              (video) => video.type == "video" && video.id != this.currVideo.id
            );
            this.files = this.videos.filter((video) => video.type == "pdf");
          }

          // this.videosPage = res.data.page;
          // this.videosTotalPages = res.data.totalPages;
        })
        .finally(() => (this.isLoading = false));
    },

    getVideoId(url) {
      console.log("url ", url);
      var regExp =
        /^.*(youtu.be\/|v\/|u\/\w\/|embed\/|watch\?v=|\&v=)([^#\&\?]*).*/;
      var match = url.match(regExp);

      if (match && match[2].length == 11) {
        console.log(match[2]);
        this.currVideoId = match[2];
        return match[2];
      } else {
        return "error";
      }
    },

    handleYoutubeAndVimeo() {
      this.videos.map((ele) => {
        if (ele.link && ele.link.includes("youtube.com")) {
          ele.link = ele.link.replace("watch?v=", "embed/");
          if (ele.link.includes("&")) {
            let index = ele.link.indexOf("&");
            ele.link = ele.link.substring(0, index);
          }
        } else if (
          ele.link &&
          ele.link.includes("vimeo.com") &&
          !ele.link.includes("player")
        ) {
          ele.link = ele.link.replace("vimeo.com/", "player.vimeo.com/video/");
        }

        console.log(ele.link);
      });
    },
  },
};
